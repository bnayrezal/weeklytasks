public class TypeCastDemo {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		// Upcasting typecasting from char to integer
		int varInt = 'a';
		System.out.println(varInt + " Upcasting");
		
		// Downcasting to char
		char varChar = (char) 110;
		
		System.out.println(varChar + " Downcasting");
		

	}

}
